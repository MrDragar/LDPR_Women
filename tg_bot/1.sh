#!/bin/bash

# Скрипт для вывода содержимого Python файлов из src в консоль

echo "========================================="
echo "СОДЕРЖИМОЕ PYTHON ФАЙЛОВ ИЗ ДИРЕКТОРИИ src"
echo "========================================="
echo

# Проверяем, существует ли директория src
if [ ! -d "src" ]; then
    echo "Ошибка: Директория src не найдена!"
    exit 1
fi

# Ищем все .py файлы в src и выводим их содержимое
find src -name "*.py" -type f | sort | while read file; do
    echo "========================================="
    echo "ФАЙЛ: $file"
    echo "========================================="
    echo
    cat "$file"
    echo
    echo
done

echo "========================================="
echo "ВЫВОД ЗАВЕРШЕН"
echo "========================================="
